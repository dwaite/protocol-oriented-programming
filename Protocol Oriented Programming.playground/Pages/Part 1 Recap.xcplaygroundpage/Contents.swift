# Part 1 Recap

> A protocol defines a blueprint of methods, properties, and other requirements that suit a particular task or piece of functionality.

* Protocol-Oriented Programming is an Object Oriented Paradigm with protocols and extensions used instead of class inheritance.
* You are meant to use both paradigms, where appropriate. However, Protocol-Oriented Programming likely will perform better and have less edge cases than class inheritance
* In addition to normal protocols, there is a concept I call "Deferred Protocols" which:
    * Have type information to be specified by the implementor
    * Do not exist at runtime
    * Are used as constraints for pattern matching/generic code
* Most of the protocols in the Swift standard library are this kind of deferred protocol

